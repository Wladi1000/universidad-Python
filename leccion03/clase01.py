#Tipos de datos
print('Tipos de datos');
x = 10;
print(x);
print(type(x)); #int

#Aplicando un hint / pista o indicacion
#El hint no define el tipo de variable, es como una etiqueta para el programador
y: str = 'Hola Mundo';
print(type(y)); #str

z = 15.4;
print(type(z)); #float

#True y False necesitan mayuscula al inicio
a = True;
print(type(a)); #bool