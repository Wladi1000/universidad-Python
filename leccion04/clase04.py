# Operadores Logicos
a = 4;
b = 2;

# igual que...
resultado = a == b;
print( f'resultado == : {resultado}');

# Mayor que...
resultado = a > b;
print( f'resultado > : {resultado}');

# Menor a...
resultado = a < b;
print( f'resultado < : {resultado}');

# Diferente de...
resultado = a != b;
print( f'resultado != : {resultado}');